package ru.properties;

public class ConfigReader {

    private String checkboxOS;
    private String checkboxLAN;
    private String checkboxACTION;

    public String getCheckboxOS() {
        return checkboxOS;
    }

    public void setCheckboxOS(String checkboxOS) {
        this.checkboxOS = checkboxOS;
    }

    public String getCheckboxLAN() {
        return checkboxLAN;
    }

    public void setCheckboxLAN(String checkboxLAN) {
        this.checkboxLAN = checkboxLAN;
    }

    public String getCheckboxACTION() {
        return checkboxACTION;
    }

    public void setCheckboxACTION(String checkboxACTION) {
        this.checkboxACTION = checkboxACTION;
    }
}
